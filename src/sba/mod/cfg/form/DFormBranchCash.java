    @Override
    public void reloadCatalogues() {
        miClient.getSession().populateCatalogue(moKeyBranch, DModConsts.BU_BRA, DLibConsts.UNDEFINED, new DGuiParams(new int[] { DUtilConsts.BPR_CO_ID }));
        miClient.getSession().populateCatalogue(moKeyType, DModConsts.CS_CSH_TP, DLibConsts.UNDEFINED, null);
        miClient.getSession().populateCatalogue(moKeyCurrency, DModConsts.CS_CUR, DLibConsts.UNDEFINED, null);
        miClient.getSession().populateCatalogue(moKeyAbpCash, DModConsts.F_ABP_CSH, DLibConsts.UNDEFINED, null);
    }

    @Override
    public void setRegistry(DDbRegistry registry) throws Exception {
        int bizPartnerId = DLibConsts.UNDEFINED;
        int branchId = DLibConsts.UNDEFINED;

        moRegistry = (DDbBranchCash) registry;

        mnFormResult = DLibConsts.UNDEFINED;
        mbFirstActivation = true;

        removeAllListeners();
        reloadCatalogues();

        bizPartnerId = moRegistry.getPkBizPartnerId();  // preserver branch key on copy
        branchId = moRegistry.getPkBranchId();          // preserver branch key on copy

        if (moRegistry.isRegistryNew()) {
            moRegistry.initPrimaryKey();
            jtfRegistryKey.setText("");

            if (moRegistry.getFkCurrencyId() == DLibConsts.UNDEFINED) {
                moRegistry.setFkCurrencyId(miClient.getSession().getSessionCustom().getLocalCurrencyKey()[0]);
            }
        }
        else {
            jtfRegistryKey.setText(DLibUtils.textKey(moRegistry.getPrimaryKey()));
        }

        moKeyBranch.setValue(new int[] { bizPartnerId, branchId });
        moTextCode.setValue(moRegistry.getCode());
        moTextName.setValue(moRegistry.getName());
        moKeyType.setValue(new int[] { moRegistry.getFkCashTypeId() });
        moKeyCurrency.setValue(new int[] { moRegistry.getFkCurrencyId() });
        moKeyAbpCash.setValue(new int[] { moRegistry.getFkAbpCashId() });

        setFormEditable(true);

        if (!moRegistry.isRegistryNew()) {
            moKeyBranch.setEditable(false);
        }

        addAllListeners();
    }

    @Override
    public DDbBranchCash getRegistry() throws Exception {
        DDbBranchCash registry = moRegistry.clone();

        if (registry.isRegistryNew()) {
            registry.setPkBizPartnerId(moKeyBranch.getValue()[0]);
            registry.setPkBranchId(moKeyBranch.getValue()[1]);
        }

        registry.setCode(moTextCode.getValue());
        registry.setName(moTextName.getValue());
        registry.setFkCashTypeId(moKeyType.getValue()[0]);
        registry.setFkCurrencyId(moKeyCurrency.getValue()[0]);
        registry.setFkAbpCashId(moKeyAbpCash.getValue()[0]);

        return registry;
    }

