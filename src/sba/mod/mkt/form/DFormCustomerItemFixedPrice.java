    @Override
    public void reloadCatalogues() {
        miClient.getSession().populateCatalogue(moKeyLinkCustomerType, DModConsts.MS_LNK_CUS_TP, DLibConsts.UNDEFINED, null);
        miClient.getSession().populateCatalogue(moKeyItem, DModConsts.IU_ITM, DLibConsts.UNDEFINED, null);
    }

    @Override
    public void setRegistry(DDbRegistry registry) throws Exception {
        int linkCustomerTypeId = DLibConsts.UNDEFINED;
        int referenceId = DLibConsts.UNDEFINED;
        int itemId = DLibConsts.UNDEFINED;

        moRegistry = (DDbCustomerItemFixedPrice) registry;

        mnFormResult = DLibConsts.UNDEFINED;
        mbFirstActivation = true;

        removeAllListeners();
        reloadCatalogues();

        linkCustomerTypeId = moRegistry.getPkLinkCustomerTypeId();
        referenceId = moRegistry.getPkReferenceCustomerId();
        itemId = moRegistry.getPkItemId();

        if (moRegistry.isRegistryNew()) {
            moRegistry.initPrimaryKey();
            jtfRegistryKey.setText("");
        }
        else {
            jtfRegistryKey.setText(DLibUtils.textKey(moRegistry.getPrimaryKey()));
        }

        setFormEditable(true);

        moKeyLinkCustomerType.setValue(new int[] { linkCustomerTypeId });
        itemStateKeyLinkCustomerType();
        moKeyReference.setValue(new int[] { referenceId });
        moKeyItem.setValue(new int[] { moRegistry.getPkItemId() });
        moCurPrice.getField().setValue(moRegistry.getPrice());

        if (!moRegistry.isRegistryNew()) {
            moKeyLinkCustomerType.setEnabled(false);
            moKeyReference.setEnabled(false);
            moKeyItem.setEnabled(false);
        }

        addAllListeners();
    }

    @Override
    public DDbCustomerItemFixedPrice getRegistry() throws Exception {
        DDbCustomerItemFixedPrice registry = moRegistry.clone();

        if (registry.isRegistryNew()) { }

        registry.setPkLinkCustomerTypeId(moKeyLinkCustomerType.getValue()[0]);
        registry.setPkReferenceCustomerId(moKeyReference.getValue()[0]);
        registry.setPkItemId(moKeyItem.getValue()[0]);
        registry.setPrice(moCurPrice.getField().getValue());

        return registry;
    }

    @Override
    public DGuiValidation validateForm() {
        DGuiValidation validation = moFields.validateFields();

        if (validation.isValid()) {
            if (moCurPrice.getField().getValue() == 0) {
                if (miClient.showMsgBoxConfirm(DTrnConsts.MSG_CNF_REG_VALUELESS) != JOptionPane.YES_OPTION) {
                    validation.setMessage(DGuiConsts.ERR_MSG_FIELD_REQ + "'" + DGuiUtils.getLabelName(jlPrice) + "'.");
                    validation.setComponent(moCurPrice.getField().getComponent());
                }
            }
        }

        return validation;
    }

    @Override
    public void itemStateChanged(ItemEvent e) {
        if (e.getSource() instanceof DBeanFieldKey) {
            if (e.getStateChange() == ItemEvent.SELECTED) {
                DBeanFieldKey field = (DBeanFieldKey) e.getSource();

                if (field == moKeyLinkCustomerType) {
                    itemStateKeyLinkCustomerType();
                }
            }
        }
    }
