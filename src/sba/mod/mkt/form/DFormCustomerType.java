    @Override
    public void reloadCatalogues() {
        miClient.getSession().populateCatalogue(moKeySegment, DModConsts.MU_SEG, DLibConsts.UNDEFINED, null);
        miClient.getSession().populateCatalogue(moKeyChannel, DModConsts.MU_CHA, DLibConsts.UNDEFINED, null);
        miClient.getSession().populateCatalogue(moKeyRoute, DModConsts.MU_ROU, DLibConsts.UNDEFINED, null);
        miClient.getSession().populateCatalogue(moKeyAgent, DModConsts.BX_BPR_AGT, DLibConsts.UNDEFINED, null);
    }

    @Override
    public void setRegistry(DDbRegistry registry) throws Exception {
        moRegistry = (DDbCustomerType) registry;

        mnFormResult = DLibConsts.UNDEFINED;
        mbFirstActivation = true;

        removeAllListeners();
        reloadCatalogues();

        if (moRegistry.isRegistryNew()) {
            moRegistry.initPrimaryKey();
            jtfRegistryKey.setText("");
        }
        else {
            jtfRegistryKey.setText(DLibUtils.textKey(moRegistry.getPrimaryKey()));
        }

        moTextName.setValue(moRegistry.getName());
        moKeySegment.setValue(new int[] { moRegistry.getFkSegmentId() });
        moKeyChannel.setValue(new int[] { moRegistry.getFkChannelId() });
        moKeyRoute.setValue(new int[] { moRegistry.getFkRouteId() });
        moKeyAgent.setValue(new int[] { moRegistry.getFkAgentId_n() });

        setFormEditable(true);

        addAllListeners();
    }

    @Override
    public DDbCustomerType getRegistry() throws Exception {
        DDbCustomerType registry = moRegistry.clone();

        if (registry.isRegistryNew()) { }

        registry.setName(moTextName.getValue());
        registry.setFkSegmentId(moKeySegment.getValue()[0]);
        registry.setFkChannelId(moKeyChannel.getValue()[0]);
        registry.setFkRouteId(moKeyRoute.getValue()[0]);
        registry.setFkAgentId_n(moKeyAgent.getSelectedIndex() <= 0 ? DLibConsts.UNDEFINED : moKeyAgent.getValue()[0]);

        return registry;
    }

    @Override
    public DGuiValidation validateForm() {
        DGuiValidation validation = moFields.validateFields();

        return validation;
    }
