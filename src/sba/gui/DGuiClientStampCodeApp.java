    private void actionGenerate() {
        DGuiValidation validation = null;
        
        validation = moDateDate.validateField();
        if (!validation.isValid()) {
            JOptionPane.showConfirmDialog(rootPane, validation.getMessage(), DGuiConsts.MSG_BOX_WARNING, JOptionPane.WARNING_MESSAGE);
            moDateDate.requestFocus();
        }
        else {
            validation = moIntQuantity.validateField();
            if (!validation.isValid()) {
                JOptionPane.showConfirmDialog(rootPane, validation.getMessage(), DGuiConsts.MSG_BOX_WARNING, JOptionPane.WARNING_MESSAGE);
                moIntQuantity.requestFocus();
            }
            else {
                jtfStampCode.setText(DGuiClientUtils.generateStampCode(moDateDate.getValue(), moIntQuantity.getValue()));
            }
        }
    }
    
    private void actionClose() {
        dispose();
    }
    
